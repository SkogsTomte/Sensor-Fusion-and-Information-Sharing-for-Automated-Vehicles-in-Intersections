road(scenario, roadCenters, 'Lanes', laneSpecification);
roadCenters = [25 20 0; 25 -20 0];
laneSpecification = lanespec(2, 'Width', 3);
road(scenario, roadCenters, 'Lanes', laneSpecification);

vehicle1 = vehicle(scenario, ...
    'ClassID', 1, ...
    'Position', [23.5 18.2 0]);
