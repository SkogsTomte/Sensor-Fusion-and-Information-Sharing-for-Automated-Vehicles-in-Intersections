trajectory(vehicle1, waypoints, speed);

vehicle2 = vehicle(scenario, ...
    'ClassID', 1, ...
    'Position', [43.5 1.6 0], ...
    'Yaw', -0);
waypoints = [43.5 1.6 0;
    6.3 1.6 0];
